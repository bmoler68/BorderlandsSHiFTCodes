package com.brianmoler.borderlandsshiftcodes.ui.theme

/**
 * Theme mode options for the Borderlands SHiFT Codes application
 * 
 * This enum defines the available theme modes that users can choose from:
 * - SYSTEM: Follow the device's system theme preference
 * - LIGHT: Always use light theme
 * - DARK: Always use dark theme
 */
enum class ThemeMode {
    SYSTEM,
    LIGHT,
    DARK
}
